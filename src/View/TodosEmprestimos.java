    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        AmigoDAO amigoDAO = new AmigoDAO();
        int ID_amigo;
        FerramentaDAO ferramentaDAO = new FerramentaDAO();
        int ID_ferramenta;

        try {
            // recebendo e validando dados da interface gr�fica.
            int ID_emprestimo = 0;
            String nome_amigo = this.tabela_emprestimo.getValueAt(this.tabela_emprestimo.getSelectedRow(), 1).toString();
            String nome_ferramenta = this.tabela_emprestimo.getValueAt(this.tabela_emprestimo.getSelectedRow(), 2).toString();
            String marca = this.tabela_emprestimo.getValueAt(this.tabela_emprestimo.getSelectedRow(), 3).toString();
            String data_ocorreu = this.tabela_emprestimo.getValueAt(this.tabela_emprestimo.getSelectedRow(), 4).toString();
            String data_devolucao = this.c_data_devolucao.getText();
            int campo_status = this.c_status.getSelectedIndex();
            
            Boolean status_emprestimo = true;
            
            if(campo_status == 1){
                status_emprestimo = true;            
            }else if(campo_status == 2){
                status_emprestimo = false;
            }
            

            //VALIDACAO
            if (this.tabela_emprestimo.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um emprestimo para Alterar");
            } else {
                ID_emprestimo = Integer.parseInt(this.tabela_emprestimo.getValueAt(this.tabela_emprestimo.getSelectedRow(), 0).toString());
            }

            //PEGANDO AMIGO
            ID_amigo = amigoDAO.BuscaAmigoByNameRetornandoID(data_devolucao);

            //PEGANDO FERRAMENTA
            ID_ferramenta = ferramentaDAO.buscaFerramentaByNomeMarca(nome_ferramenta, marca).getID_ferramenta(); 
            
            // envia os dados para o emprestimo processar
            if (this.objEmprestimo.UpdateEmprestimoBD(ID_emprestimo, ID_amigo, ID_ferramenta, data_ocorreu, data_devolucao, status_emprestimo)) {             
                // limpa os campos
                this.c_data_devolucao.setText("");
                this.c_status.setSelectedIndex(0);
                JOptionPane.showMessageDialog(rootPane, "Emprestimo Alterado com Sucesso!");

            }
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um n�mero.");
        } finally {
            carregaTabela(); // atualiza a tabela.
        }
    }//GEN-LAST:event_jButton2ActionPerformed

