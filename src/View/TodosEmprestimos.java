    @SuppressWarnings("unchecked")
    private void carregaTabela() {
        AmigoDAO amigoDAO = new AmigoDAO();
        Amigo amigo = new Amigo();
        FerramentaDAO ferramentaDAO = new FerramentaDAO();
        Ferramenta ferramenta = new Ferramenta();

        DefaultTableModel modelo = (DefaultTableModel) this.tabela_emprestimo.getModel();
        modelo.setNumRows(0);

        ArrayList<Emprestimo> emprestimos = new ArrayList<Emprestimo>();
        emprestimos = objEmprestimo.visualizaEmprestimos();

        for (int i = 0; i < emprestimos.size(); i++) {
            amigo = amigoDAO.carregaAmigo(i + 1);
            ferramenta = ferramentaDAO.carregaFerramenta(i + 1);

            modelo.addRow(new Object[]{
                emprestimos.get(i).getID_emprestimo(),
                amigo.getNome_amigo(),
                ferramenta.getNome_ferramenta(),
                ferramenta.getMarca(),
                emprestimos.get(i).getData_ocorreu(),
                emprestimos.get(i).getData_devolucao(),
                emprestimos.get(i).getStatus_emprestimo(),
            });
        }
    }

