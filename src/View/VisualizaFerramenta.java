    @SuppressWarnings("unchecked")
    private void carregaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) this.tabela_ferramenta.getModel();
        modelo.setNumRows(0);

        ArrayList<Ferramenta> visualizaferramentas = new ArrayList<>();
        visualizaferramentas = objferramenta.getVisualizaFerramentas();

        for (Ferramenta a : visualizaferramentas) {
            modelo.addRow(new Object[]{
                a.getID_ferramenta(),
                a.getNome_ferramenta(),
                a.getMarca(),
                a.getCusto_aquisicao(),});
        }
    }

