    private void b_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_alterarActionPerformed
        try {
            // recebendo e validando dados da interface gr�fica.
            int ID_ferramenta = 0;
            String nome_ferramenta = "";
            String marca = "";
            double custo_aquisicao = 0.0;

            if (this.c_ferramenta.getText().length() < 2) {
                throw new Mensagens("Ferramenta deve conter ao menos 2 caracteres.");
            } else {
                nome_ferramenta = this.c_ferramenta.getText();
            }

            if (this.c_marca.getText().length() < 2) {
                throw new Mensagens("Marca deve conter ao menos 2 caracteres.");
            } else {
                marca = this.c_marca.getText();
            }

            if (this.c_custoAquisicao.getText().length() <= 0) {
                throw new Mensagens("Custo de Aquisição deve ser n�mero e maior que zero.");
            } else {
                custo_aquisicao = Double.parseDouble(this.c_custoAquisicao.getText());
            }

            if (this.tabela_ferramenta.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione uma Ferramenta para Alterar");
            } else {
                ID_ferramenta = Integer.parseInt(this.tabela_ferramenta.getValueAt(this.tabela_ferramenta.getSelectedRow(), 0).toString());
            }

            // envia os dados para a Ferramenta processar
            if (this.objferramenta.UpdateFerramentaBD(ID_ferramenta, nome_ferramenta, marca, custo_aquisicao)) {

                // limpa os campos
                this.c_ferramenta.setText("");
                this.c_marca.setText("");
                this.c_custoAquisicao.setText("");
                JOptionPane.showMessageDialog(rootPane, "Ferramenta Alterada com Sucesso!");

            }
            System.out.println(this.objferramenta.getVisualizaFerramentas().toString());
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um n�mero.");
        } finally {
            carregaTabela(); // atualiza a tabela.
            FerramentaDAO ferramentaDAO = new FerramentaDAO();
            this.c_totalGasto.setText(ferramentaDAO.VisualizaValorFerramentas());
        }
    }//GEN-LAST:event_b_alterarActionPerformed

    private void b_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_apagarActionPerformed
        try {
            // validando dados da interface gr�fica.
            int ID_ferramenta = 0;
            if (this.tabela_ferramenta.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione uma Ferramenta para APAGAR");
            } else {
                ID_ferramenta = Integer.parseInt(this.tabela_ferramenta.getValueAt(this.tabela_ferramenta.getSelectedRow(), 0).toString());
            }

            // retorna 0 -> primeiro bot�o | 1 -> segundo bot�o | 2 -> terceiro bot�o
            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR esta Ferramenta ?");

            if (resposta_usuario == 0) {// clicou em SIM

                // envia os dados para a Ferramenta processar
                if (this.objferramenta.DeleteFerramentaBD(ID_ferramenta)) {

                    // limpa os campos
                    this.c_ferramenta.setText("");
                    this.c_marca.setText("");
                    this.c_custoAquisicao.setText("");
                    JOptionPane.showMessageDialog(rootPane, "Ferramenta Apagada com Sucesso!");

                }

            }

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally {
            // atualiza a tabela.
            carregaTabela();
            FerramentaDAO ferramentaDAO = new FerramentaDAO();
            this.c_totalGasto.setText(ferramentaDAO.VisualizaValorFerramentas());
        }
    }//GEN-LAST:event_b_apagarActionPerformed

